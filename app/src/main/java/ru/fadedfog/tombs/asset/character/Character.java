package ru.fadedfog.tombs.asset.character;


public class Character {
	private String name;
	private int hearts;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	public int getHearts() {
		return hearts;
	}

	public void setHearts(int hearts) {
		this.hearts = hearts;
	}

}
